import java.util.Scanner;

/**
 * B_Following_the_String
 */
public class B_Following_the_String {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int z = sc.nextInt();
        for(int p=0;p<z;p++){
            int n = sc.nextInt();
            int[] arr=new int[n];
            for (int i = 0; i < n; i++) {
                arr[i]=sc.nextInt();
            }
            
        }
    }
}